/* 

return opcode for an instruction and addressing mode 
if not found return -1

*/

int GetOpCode(const int instruction, const int addressingMode)
{

    if ((instruction >= 0 && instruction < _maxOpcode) &&
        (addressingMode >= 0 && addressingMode < (maxAddressingMode + 1)))
    {
        if (!AllowIllegalOpCodes && instruction >= _illegalStart)
            return -1;

        switch (CPUMode)  // NOLINT(hicpp-multiway-paths-covered)
        {
            case cpu_6502:
                return Ops6502[instruction][addressingMode + 1];
 
            case cpu_65C02:
                return Ops65C02[instruction][addressingMode + 1];
        }
    }
    return -1;
}

/// <summary>
/// Instruction to string.
/// </summary>
/// <param name="instruction">The instruction.</param>
/// <returns>char *.</returns>
char * InstructionToString(const int instruction)
{
    if (instruction < _maxOpcode)
        return InstrLookUp[instruction].name;
    return "Unknown";
}

/// <summary>
/// Mode to string.
/// </summary>
/// <param name="mode">The mode.</param>
/// <returns>char *.</returns>
char * ModeToString(const int mode)
{
    if (mode < maxAddressingMode)
        return MdLookUp[mode].name;
    return "Unknown";
}
